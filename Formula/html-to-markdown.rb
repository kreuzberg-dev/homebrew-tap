      sha256 "703ff5c6d2bf6c7b09a78178ae4420361a08cd3bfa43a0eacc04dbb4fb31773f"
    end

    on_intel do
      url "https://github.com/kreuzberg-dev/html-to-markdown/releases/download/v#{version}/cli-x86_64-apple-darwin.tar.gz"
