      url "https://github.com/kreuzberg-dev/html-to-markdown/releases/download/v#{version}/cli-aarch64-unknown-linux-gnu.tar.gz"
      sha256 "5ea68e601f4194837d8158decb7e7fc6f2a1eedc490a0ffbf0e4635d17940d4d"
